namespace Nirvana.TestAutomation.Steps.Closing
{
    class UnWindCloseOrder : CloseOrderTabUIMAP, ITestStep
    {
        /// <summary>
        /// Runs the test.
        /// </summary>
        /// <param name="testData">The test data.</param>
        /// <returns></returns>
        public TestResult RunTest(DataSet testData, Dictionary<int, string> sheetIndexToName)
        {
            TestResult _result = new TestResult();
            try
            {
                CloseOrderTab.Click(MouseButtons.Left);
                ClosingCommonMethods.UnwindClosingData(testData, sheetIndexToName, GrdCloseTrades);
                Unwind.Click(MouseButtons.Left);
                ButtonYes.Click(MouseButtons.Left);
        
            }
            catch (Exception ex)
            {
                _result.IsPassed = false;
                bool rethrow = ExceptionPolicy.HandleException(ex, ExceptionHandlingConstants.LOG_AND_CAPTURE_POLICY);
                if (rethrow)
                    throw;
            }
            finally
            {
                MinimizeClosing();
            }
            return _result;
        }
        /// <summary>
        /// Disposes resources
        /// </summary>
        /// <param name="disposing"></param>
        protected override void Dispose(bool disposing)
        {
            base.Dispose(true);
            GC.SuppressFinalize(this);
        }
    }
}
